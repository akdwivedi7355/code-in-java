public class A19_substring {
    public static void main(String[] args) {
        String sentence = "my name is Aditya Kumar Dwivedi";
        //  substring(base index,ending index)
        System.out.println(sentence.substring(11, sentence.length()));
        String name = "Aditya Kumar Dwivedi";
        System.out.println(name.substring(7, 11));
    }
}
